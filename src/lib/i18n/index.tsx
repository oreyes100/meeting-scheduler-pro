export function getInitialLocale(): Locale {
  if (typeof window === 'undefined') return 'en';
  try {
    const stored = window.localStorage.getItem(STORAGE_KEY) as Locale | null;
    if (stored === 'en' || stored === 'es') return stored;
  } catch {
    // ignore
  }
  // Fall back to browser language
  const browser = (typeof navigator !== 'undefined' && navigator.language) || 'en';
  if (browser.toLowerCase().startsWith('es')) return 'es';
  return 'en';
}

export function persistLocale(locale: Locale): void {
  if (typeof window === 'undefined') return;
  try {
    window.localStorage.setItem(STORAGE_KEY, locale);
  } catch {
    // ignore
  }
}

// ─── React context ─────────────────────────────────────────────────────────────
interface LocaleContextValue {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
}

const LocaleContext = createContext<LocaleContextValue | null>(null);

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>('en');

  useEffect(() => {
    // eslint-disable-next-line react-hooks/set-state-in-effect
    setLocaleState(getInitialLocale());
  }, []);

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    persistLocale(l);
  }, []);

  const t = useCallback(
    (key: string, params?: Record<string, string | number>) => {
      const template = translations[locale]?.[key] ?? translations.en[key] ?? key;
      if (!params) return template;
      return Object.entries(params).reduce(
        (acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)),
        template,
      );
    },
    [locale],
  );

  return React.createElement(LocaleContext.Provider, { value: { locale, setLocale, t } }, children);
}

export function useLocale(): LocaleContextValue {
  const ctx = useContext(LocaleContext);
  if (!ctx) {
    // Fallback for components rendered outside the provider (e.g. server-rendered)
    return {
      locale: 'en',
      setLocale: () => {},
      t: (key: string, params?: Record<string, string | number>) => {
        const template = translations.en[key] ?? key;
        if (!params) return template;
        return Object.entries(params).reduce(
          (acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)),
          template,
        );
      },
    };
  }
  return ctx;
}

